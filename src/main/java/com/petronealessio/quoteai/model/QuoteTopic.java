package com.petronealessio.quoteai.model;

/**
 * The QuoteTopic enum represents different topics for generating quotes.
 * Each topic corresponds to a specific category of quotes.
 */
public enum QuoteTopic {
    /**
     * The default topic for generating quotes.
     */
    DEFAULT,

    /**
     * The motivational topic for generating motivational quotes.
     */
    MOTIVATIONAL,

    /**
     * The humoristic topic for generating humorous quotes.
     */
    HUMORISTIC,

    /**
     * The love topic for generating quotes about love.
     */
    LOVE,

    /**
     * The existential topic for generating existential quotes.
     */
    EXISTENTIAL,

    /**
     * The friendship topic for generating quotes about friendship.
     */
    FRIENDSHIP
}
